package io.github.codexrm.jris;

public class Conf extends RisEntry {
  
  private String vl;
  private String t3;
  private String ad;

  public Conf() {
    super();
  }
  
  public String getVl() {
    return vl;
  }

  public void setVl(String vl) {
    this.vl = vl;
  }

  public String getT3() {
    return t3;
  }

  public void setT3(String t3) {
    this.t3 = t3;
  }

  public String getAd() {
    return ad;
  }

  public void setAd(String ad) {
    this.ad = ad;
  }
}
